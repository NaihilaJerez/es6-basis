export const restar=function(a,b){
    return a-b 
}
//sintaxis legada 
export function multiplicar(a,b){
    return a*b 
}
//sintaxis identificador 
export const dividir=function(a,b){
    return a/b 
}
//sintaxis identificador 
export const elevar=function(a,b){
    return a^b 
}
